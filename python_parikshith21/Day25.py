D={'tv':1000,'freezer':5000,'washing machine':2500}
print(D) 
# =============================================================================
# #output:
# {'tv': 1000, 'freezer': 5000, 'washing machine': 2500}
# =============================================================================

D['tv']
print(D['tv'])  
# =============================================================================
# #output: 
# 1000
# =============================================================================

D['freezer']=6000
print(D)  
# =============================================================================
# #output:
# {'tv': 1000, 'freezer': 6000, 'washing machine': 2500}
# =============================================================================

D['mobile']=10000
print(D) 
# =============================================================================
# #output:
# {'tv': 1000, 'freezer': 6000, 'washing machine': 2500, 'mobile': 10000}
# =============================================================================

 
D={'tv':5000,'freezer':2000}
for key in D:
    print(key)
# =============================================================================
# #output:
# tv
# freezer
# =============================================================================

 
#Program to print all the values in a dictionary using for loop
D={'tv':5000,'freezer':2000}
for key in D:
    print(D[key])
# =============================================================================
# #output: 
# 5000        
# 2000
# =============================================================================
 