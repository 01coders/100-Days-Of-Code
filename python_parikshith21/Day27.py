#Progarm to print the occurence count of all the characters in a string
str="Good Morning"
d=dict()
for ch in str:
    if ch in d:
        d[ch]=d[ch]+1
    else:
        d[ch]=1
print(d)
# =============================================================================
# #output:
# {'G': 1, 'o': 3, 'd': 1, ' ': 1, 'M': 1, 'r': 1, 'n': 2, 'i': 1, 'g': 1}
# =============================================================================
 
  
