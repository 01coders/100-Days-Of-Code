#Program to print al items having price greater than 2500
D={'tv': 5000, 'freezer': 2000, 'washing machine': 3000}
for key in D:
    if (D[key] > 2500):
        print(key,":",D[key])
# =============================================================================
# #output:
# tv : 5000
# washing machine : 3000
