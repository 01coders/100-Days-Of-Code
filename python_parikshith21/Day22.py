import re

fhand=open('regular_expression_input.txt')
 
for line in fhand:
    print(line)
    if re.search('X\S*:[0-9.]+',line):
        print(line)
 
# =============================================================================
# #output:
# Xyz: 808.3
# 
# Xyz: 808.3.3.3
# 
# ABXyz: 808.3.3
# 
# this is ABXyz: 808.3.3 thank you
# 
# this is ABXyz: 808 thank you
# =============================================================================

 
for line in fhand:
    if re.search('X\S*: [0-9]+[.][0-9]+',line):
        print(line)
 
# =============================================================================
# #output:
# Xyz: 808.3
# 
# Xyz: 808.3.3.3
# 
# ABXyz: 808.3.3
# 
# this is ABXyz: 808.3.3 thank you
# =============================================================================
    

for line in fhand:
    if re.match('X\S*: [0-9]+',line): #match will match pattern at begining only
        print(line)
# =============================================================================
# #output:
# Xyz: 808.3
# 
# Xyz: 808.3.3.3
# =============================================================================

for line in fhand:
    L=re.findall('[0-9]+',line)
    print(L)
